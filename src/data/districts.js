const currentDistrictSet = new Set(currentDistrictIds);
const annulledDistrictSet = new Set(annulled2024DistrictIds);

export const districts = districtSeed.map((district) => {
  const enhancement = districtEnhancements[district.id] || {};
  const isCurrent = currentDistrictSet.has(district.id);
  const isAnnulled = annulledDistrictSet.has(district.id);
  const merged = {
    ...district,
    ...enhancement,
    famousFor: enhancement.famousFor || district.famousFor,
  };

  return {
    ...merged,
    administrativeStatus: isCurrent ? "Current district" : isAnnulled ? "Annulled 2024-25 district" : "Historical/legacy map district",
    area:
      merged.area === "To be added"
        ? "Use current district gazetteer after 2024-25 boundary finalisation"
        : merged.area,
    population:
      merged.population === "To be added"
        ? "Latest comparable district population awaits post-2011 Census/current boundary publication"
        : merged.population,
    nickname: merged.nickname === "To be added" ? `${merged.region} district memory` : merged.nickname,
    ancientName:
      merged.ancientName === "To be added"
        ? `${merged.region} historical-region memory`
        : merged.ancientName,
    borderInfo:
      merged.borderInfo === "To be added"
        ? "See neighboring districts on the interactive map; state/international border noted where applicable"
        : merged.borderInfo,
    famousFor: (merged.famousFor || []).filter((item) => item !== "To be added").length
      ? (merged.famousFor || []).filter((item) => item !== "To be added")
      : [`${merged.region} region`, merged.importantCity],
  };
});

export const getDistrictById = (id) =>
  districts.find((district) => district.id === id);
