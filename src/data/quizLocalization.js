export function localizeQuizzes(items, language) {
  if (language !== "hi") return items;

  return items.map((item) => {
    const options = item.options?.map(localizeText) || [];
    const correctAnswer = localizeText(item.correctAnswer);
    return {
      ...item,
      question: QUESTION_HI[item.id] || "सही विकल्प चुनें.",
      options,
      correctAnswer,
      explanation: `सही उत्तर: ${correctAnswer}.`,
    };
  });
}

export function localizeMatchQuizzes(items, language) {
  if (language !== "hi") return items;

  return items.map((item) => {
    const [title, prompt] = MATCH_HI[item.id] || [item.title, item.prompt];
    return {
      ...item,
      title,
      prompt,
      pairs: item.pairs.map((pair) => ({
        left: localizeText(pair.left),
        right: localizeText(pair.right),
      })),
      explanation: "मिलान को स्थान, तथ्य और स्मृति संकेत के साथ दोहराएं.",
    };
  });
}

export function localizeMapDistrictQuizzes(items, language) {
  if (language !== "hi") return items;

  return items.map((item) => ({
    ...item,
    prompt: MAP_PROMPT_HI[item.id] || "सही जिले पर क्लिक करें.",
    explanation: "मानचित्र पर सही जिला पहचानना इस तथ्य को याद रखने में मदद करता है.",
  }));
}

function localizeText(value) {
  return OPTION_HI[value] || toHindiDigits(value);
}

function toHindiDigits(value) {
  const digits = ["०", "१", "२", "३", "४", "५", "६", "७", "८", "९"];
  return String(value).replace(/\d/g, (digit) => digits[Number(digit)]);
}
